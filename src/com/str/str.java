package com.str;

public class str{
	public String string;
	public int id;
	public str(){}
	public str(String str){
		this.string=str;
	}
	public void setstring(String str){
		this.string=str;
	}
	public String getstring(){
		return this.string;
	}
	public void setid(int id){
		this.id=id;
	}
	public int getid(){
		return this.id;
	}
}
